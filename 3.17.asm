.586 
STACKS	SEGMENT	USE16
		DB	256	DUP(0)
STACKS ENDS

DATAS	SEGMENT	USE16
	N	EQU	26
	BUF	DB	N+1
	COUNT	DB	0
	CHAR	DB	N+1 DUP(0)
			DB 10
			DB 13
			DB '$'
	PROMPT	DB	'PLEASE INPUT YOUR STRING:',13,10,'$'
	;NEXT    DB  13,10,'$'
	RESTIP  DB  'THE STRING YOU INPUT IS:',13,10,'$'
DATAS	ENDS	

CODES	SEGMENT	USE16
		ASSUME	CS:CODES,DS:DATAS
	START:	
		MOV	AX,DATAS;段地址->AX
		MOV	DS,AX	;段地址->DS
		LEA	DX,prompt	;取PROMPT的偏移地址->DX
		
		MOV	AH,9	;显示输入提示字符串
		INT 21H
		
		LEA DX,BUF	;缓冲区输入
		MOV AH,0AH
		INT 21H
		
		MOV DL,10
		MOV AH,2
		INT 21H
			
		LEA DX,restip
		MOV AH,9
		INT 21H	
		
		LEA DX,CHAR
		MOV SI,DX
		MOV BH,0
		MOV BL,[SI-1]
		MOV BYTE PTR [SI+BX],'$'
		
		LEA DX,CHAR
		MOV AH,9
		INT 21H
		
		
		MOV AH,4CH
		INT 21H
CODES ENDS
	END START
	





